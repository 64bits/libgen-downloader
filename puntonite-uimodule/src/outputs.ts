OUTPUTS.TOGGLEDOUTPUT = `${ascii.YELLOW}${OUTPUTS.STANDARTPREFIX} ─ {text}${ascii.RESETCOLOR}\n`;
OUTPUTS.TOGGLEDHOVEREDOUTPUT = `${ascii.BRIGHTYELLOW}${OUTPUTS.HOVERPREFIX} ─ {text}${ascii.RESETCOLOR}\n`;

export default OUTPUTS;